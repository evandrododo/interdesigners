<h2>o que são? onde vivem? o que comem?</h2>
<p>
	Você tem uma marca própria e quer divulgar pra todo mundo? É uma comissão ou 
	pré-comissão e quer encher seu evento de gente legal, e ainda por cima quer levantar 
	uma graninha? Esse edital é pra você que tem seus produtos é quer vendê-los!
</p>
<p>
	O edital de bazar é o espaço para vender seus produtos durante o evento, em um 
	local pré-determinado pela CORDe para todo e qualquer encontrista com grana no bolso 
	e desejo na mente!
</p>
<p>
	Aqui você é livre para vender todo e qualquer produto voltado ao público de design, 
	desde que este não seja ilícito. Além disso, a venda de bebidas alcoólicas não é permitida 
	dentro do Campus. Frisamos que caso um bazar seja pego vendendo bebidas alcoolicas, 
	este será banido do evento, juntamente com o responsável pelo bazar. A CORDe se 
	responsabiliza por disponibilizar um local de exposição e sua limpeza ao final do evento. 
	A cargo de cada marca, fica a responsabilidade de dispor seus produtos nos locais 
	indicados e de sempre ter alguém tomando conta deles enquanto estiverem dispostos. 
	A carga horária desse edital é livre, então você pode expor a hora e o tempo que quiser, 
	respeitando o horário máximo colocado pela CORDe.
</p>
<h2>critérios</h2>
<ul>
	<li>Inscrever-se no prazo estipulado neste edital</li>
	<li>Adequação com o público do evento</li>
	<li>Itens a serem comercializados e preços</li>
	<li>Credibilidade da marca</li>
</ul>
<p>Os critérios omissos aos aqui apresentados serão julgados pela CORDe, que possui autonomia para selecionar o candidato de acordo com o que mais for proveitoso. Esperamos selecionar os melhores para que o evento seja incrível!</p>
<h2>é de grátis?</h2>
<p>
	Esse edital não possui isenção, pois encaramos como uma cooperação mútua, 
	onde o participante tem o benefício de vender seus produtos, como meio de divulgação
</p>
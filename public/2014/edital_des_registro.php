<h2>o que são? onde vivem? o que comem?</h2>
<p>
A monitoria de registro é uma das mais importantes dos eventos de Design. Cada 
um tem a sua função pra fazer o evento acontecer, a função do Monitor de Registro é 
fazer o evento “parar no tempo” e guardar as memórias pra todo mundo poder matar 
as saudades depois. Se você gosta de fotografia e acha que ajudaria a CORDe R Misto a 
registrar o encontro, então esse é o seu edital!
</p>
<p>
Lembre-se que você será parte da organização nos dias do R Misto e como tal tem 
que pensar e agir de acordo com os pilares que a CORDe construiu até então. Você será 
um dos braços mais importantes para o encontro, sendo assim a sua responsabilidade 
vai ser grande. Por desempenhar funções essenciais para nosso encontro, contaremos 
com disposição, bom humor e boa vontade para esses dias de muito trabalho. Para que 
juntos façamos o evento acontecer da melhor maneira possível, precisamos ter certeza 
que você está com a gente.
</p>
<h2>critérios</h2>
<ul>
	<li>possuir equipamento fotográfico (câmera DSLR e flash para quem for fotografar as festas)</li>
	<li>habilidades fotográficas e avaliação de portfólio</li>
	<li>flexibilidade quanto ao turno de trabalho</li>
	<li>organização e pontualidade</li>
	<li>dedicação com o evento</li>
	<li>pró-atividade e disposição</li>
	<li>inscrever-se no prazo estipulado neste edital</li>
	<li>trabalhar em grupo</li>
	<li>disponibilidade para contato pré-evento (e-mail e redes sociais)</li>
	<li>possibilidade de estar em Bauru com antecedência</li>
	<li>experiência em outros eventos</li>
	<li>educação para lidar com pessoas (que nem sempre serão educadas com você)</li>
</li>
</ul>
<p>Os critérios omissos aos aqui apresentados serão julgados pela CORDe, que possui autonomia para selecionar o candidato de acordo com o que mais for proveitoso.</p>
<p>Esperamos selecionar os melhores para que o evento seja incrível!</p>
<h2>é de grátis?</h2>
<p>
	A cada monitor de registro selecionado será concedida a isenção da inscrição básica, pacote de alojamento e pacote de festas (para os monitores com escalação 
	nas festas). A isenção é intransferível e não cumulativa com outros editais. No caso de 
	seleção em dois ou mais editais, será considerado para fins de isenção o edital com maior 
	número de pacotes isentos.
</p>
<p>
	No ato do credenciamento, cada monitor deverá, obrigatoriamente, deixar um cheque caução no valor referente aos custos dos 
	pacotes isentos. O cheque caução poderá ser recuperado após o cumprimento das 
	atividades acertadas em sua íntegra com o integrante da CORDe responsável por essa 
	atividade.
</p>
<p>
	Caso haja desistência ou o monitor não cumpra com sua responsabilidade 
	durante o evento, o cheque caução será descontado. Caso ocorra alguma desistência 
	ou impossibilidade de participação no evento, e o candidato selecionado na segunda 
	chamada já tenha realizado o pagamento da inscrição como encontrista, este poderá 
	solicitar o ressarcimento e receber o valor original (sem taxas bancárias ou parcelamento) equivalente ao seu direito de isenção, apenas durante o acontecimento do R Misto 
	mediante a apresentação dos documentos de pagamento. Esse valor será equivalente ao 
	pacote pago durante as inscrições.
</p>
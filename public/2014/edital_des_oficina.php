<h2>o que são? onde vivem? o que comem?</h2>
<p>
	As oficinas estão aí, com as mais diversas propostas nas quais são trabalhadas 
	habilidades específicas e/ou desenvolvimento de atividades práticas relacionadas à 
	temática do evento e também ao vasto campo do design. São atividades ministradas 
	de encontrista para encontrista, apresentam uma estrutura horizontal e proporcionam 
	uma interação mais próxima entre os participantes, além da troca de conhecimento e 
	experiências, é claro.
</p>
<p>
	As oficinas são realizadas presencialmente nos dias do evento, em locais e horários 
	estabelecidos pela CORDe de acordo com a grade horária do evento. Os materiais serão 
	disponibilizados de acordo com o solicitado.
</p><p>
	Elas deverão durar no mínimo 2 horas e no máximo 4 horas por dia (caso seja 
	necessário mais tempo, dividiremos a oficina em 2 dias). O número de Oficineiros na 
	atividade fica por conta de quem está organizando, então, se quiser, pode chamar 
	alguém para te ajudar. Entretanto, a isenção do edital valerá apenas para uma pessoa. 
	O número de encontristas fica a critério do Oficineiro, mas também poderá ser adaptado 
	de acordo com a quantidade de pessoas que cabem dentro das salas onde as oficinas 
	serão realizadas.
</p>
<h2>critérios</h2>
<ul>
	<li>Dedicação</li>
	<li>Pró-atividade e disposição</li>
	<li>Espírito de Equipe</li>
	<li>Flexibilidade de horários</li>
	<li>Flexibilidade no número de participantes</li>
	<li>Disponibilidade para contato pré-evento (e-mail ou redes sociais)</li>
	<li>Organização e pontualidade</li>
	<li>Responder corretamente o formulário de inscrição do edital</li>
	<li>Pertinência à temática do evento</li>
	<li>Originalidade</li>
	<li>Viabilidade</li>
	<li>Interatividade</li>
</ul>
<p>
	Com base na avaliação do formulário de inscrição do edital, inclusive com avaliação 
	de portfólio e redes sociais (se disponível), os editais serão avaliados pela CORDe nessa 
	ordem de importância, para fins de desempate.
	É permitida ao oficineiro a inscrição de mais de uma oficina desde que 
	cumpra com os itens indicados anteriormente.
</p>
<p>Os critérios omissos aos aqui apresentados serão julgados pela CORDe, que possui autonomia para selecionar o candidato de acordo com o que mais for proveitoso. Esperamos selecionar os melhores para que o evento seja incrível!</p>
<h2>é de grátis?</h2>
<p>
	Será concedida a isenção da inscrição básica para este edital. A isenção é intransferível e não cumulativa com outros editais. No caso de 
	seleção em dois ou mais editais, será considerado para fins de isenção o edital com maior número de pacotes isentos.
</p>
<p>
	No ato do credenciamento, o aprovado deverá, obrigatoriamente, deixar um cheque caução no valor referente aos custos dos 
	pacotes isentos. O cheque caução poderá ser recuperado após o cumprimento das atividades acertadas em sua íntegra com o integrante da CORDe responsável por essa atividade.
</p>
<p>
	Caso haja desistência ou o aprovado não cumpra com sua responsabilidade 
	durante o evento, o cheque caução será descontado. Caso ocorra alguma desistência 
	ou impossibilidade de participação no evento, e o candidato selecionado na segunda 
	chamada já tenha realizado o pagamento da inscrição como encontrista, este poderá 
	solicitar o ressarcimento e receber o valor original (sem taxas bancárias ou parcelamento) equivalente ao seu direito de isenção, apenas durante o acontecimento do R Misto 
	mediante a apresentação dos documentos de pagamento. Esse valor será equivalente ao 
	pacote pago durante as inscrições.
</p>
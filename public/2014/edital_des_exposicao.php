<h2>o que são? onde vivem? o que comem?</h2>
<p>
	A mostra de trabalhos é o local onde você pode expor seus trabalhos de Design para que os outros vejam. Fez algo legal? Bonito? Por que não compartilhar? Aqui em Bauru ninguém tem vergonha não, pode vir e se mostrar que a gente promete que não vai passar batido!
</p>
<p>
	A exposição pretende ser um apanhado dos melhores trabalhos da região (e quem sabe de fora também!!) organizados em um espaço acessível durante toda a duração do evento, então por que não né? Teremos 5 categorias: Gráfico, Produto, Moda, Fotografia e Ambientes. A CORDe fornecerá o local e estrutura para a mostra, cabendo ao aluno/profissional trazer seu trabalho em protótipo, representação gráfica ou impresso. Também faremos a curadoria dos trabalhos e a seleção por áreas. Então tá esperando o que?
</p>
<p>
	Cada participante terá direito a inscrever 3 trabalhos por área, sendo que a CORDe pode selecionar apenas parte deles em seu processo de curadoria,
	dependendo da adequação com o conjunto da exposição e qualidade do trabalho. 
</p>
<ul>
	<li>Inscrever-se no prazo estipulado neste edital</li>
	<li>Apresentação do trabalho</li>
	<li>Originalidade ou caráter inovador</li>
	<li>Desenvoltura em sua realização</li>
	<li>Qualidade da técnica</li>
</ul>
<p>Os critérios omissos aos aqui apresentados serão julgados pela CORDe, que possui autonomia para selecionar o candidato de acordo com o que mais for proveitoso. Esperamos selecionar os melhores para que o evento seja incrível!</p>
<h2>é de grátis?</h2>
<p>
	Esse edital não possui isenção, pois encaramos como uma cooperação mútua, onde o participante tem o benefício de ter seus trabalhos expostos, como meio de divulgação.
</p>
<h2>o que são? onde vivem? o que comem?</h2>
<p>
	Fez aquele trabalhinho maneiro que todo mundo na faculdade pagou um pau, a 
	mamãe ficou orgulhosa e até abriu portas pra uns freelas legais? Vem cá mostrar pra gente! 
	O SePA e os SePP são espaços que a CORDe abre pra você contar um pouco pra galera 
	sobre o seu trabalho, desde a idéia inicial até o resultado. Você pode contar o processo 
	criativo, de manufatura ou até mesmo sobre inspirações! Apresente seu trabalho!
</p>
<p>
	Os Seminários de Produção Acadêmica (SePA) abrangem trabalhos de conclusão 
	de curso e quaisquer projetos de disciplinas que são produzidos na universidade, durante 
	a graduação. Já os Seminários de Produção Profissional (SePP) compreendem trabalhos 
	realizados fora do meio acadêmico.
</p>
<p>
	Cada selecionado terá de 20 a 30 minutos para se apresentar, havendo um tempo
	extra de 10 minutos para perguntas e comentários dos ouvintes, caso estes ocorra.
</p>
<p>
	<strong>DICA:</strong> Seria interessante se você enviasse seu trabalho acadêmico para o <a href='simposio/submissao'>simpósio</a> também, não? Teremos <strong>ISBN</strong> e isso contará pontinhos em seu currículo!
</p>
<h2>critérios</h2>
<ul>
	<li>Inscrever-se no prazo estipulado neste edital</li>
	<li>Adequação ao tema do evento</li>
	<li>Contextualização no meio acadêmico ou professional</li>
	<li>Metodologia aplicada</li>
	<li>Resultado final</li>
	<li>Proposta de discurso</li>
</ul>
<p>
	Com base na avaliação do formulário de inscrição do edital, inclusive com avaliação 
	de portfólio e redes sociais (se disponível), os editais serão avaliados pela CORDe nessa 
	ordem de importância, para fins de desempate.
	É permitida aos oficineiros a inscrição de mais de uma oficina desde que 
	cumpra com os itens indicados anteriormente.
</p>
<p>Os critérios omissos aos aqui apresentados serão julgados pela CORDe, que possui autonomia para selecionar o candidato de acordo com o que mais for proveitoso. Esperamos selecionar os melhores para que o evento seja incrível!</p>
<h2>é de grátis?</h2>
<p>
Esse edital oferece isenção do pacote básico de inscrição no evento. A isenção é 
intransferível e não cumulativa com outros editais. No caso de seleção em dois ou mais 
editais, será considerado para fins de isenção o edital com maior número de pacotes 
isentos.</p>
<p>
	No ato do credenciamento, o aorivado deverá, obrigatoriamente, deixar um cheque caução no valor referente aos custos dos 
	pacotes isentos. O cheque caução poderá ser recuperado após o cumprimento das atividades acertadas em sua íntegra com o integrante da CORDe responsável por essa atividade.
</p>
<p>
	Caso haja desistência ou o aprovado não cumpra com sua responsabilidade 
	durante o evento, o cheque caução será descontado. Caso ocorra alguma desistência 
	ou impossibilidade de participação no evento, e o candidato selecionado na segunda 
	chamada já tenha realizado o pagamento da inscrição como encontrista, este poderá 
	solicitar o ressarcimento e receber o valor original (sem taxas bancárias ou parcelamento) equivalente ao seu direito de isenção, apenas durante o acontecimento do R Misto 
	mediante a apresentação dos documentos de pagamento. Esse valor será equivalente ao 
	pacote pago durante as inscrições.
</p>
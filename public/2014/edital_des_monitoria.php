<h2>o que são? onde vivem? o que comem?</h2>
<p>
	O monitor é aquele encontrista que se destaca pela pró-atividade, facilidade de 
	se relacionar com outras pessoas, conhecimentos técnicos e paixão pelo evento tanto 
	quanto a CORDe. Você pode ajudar na construção do evento conjuntamente com a 
	CORDe, dar o seu melhor para que o evento também seja o melhor possível.
</p>
<p>
	As atividades dos monitores começam oficialmente no primeiro dia e terminam no último. 
	Suas tarefas no evento serão especificadas mais adiante, mas algumas coisas são 
	de praxe, como:
</p>
<ul>
	<li>Fornecer todo tipo de informação aos encontristas sobre funcionamento do evento, localização de atividades, dinâmicas, etc</li>
	<li>Recepcionar palestrantes e demais convidados, guiando-os até o local de suas atividades, fazer com que sejam tratados da melhor maneira possível</li>
	<li>Auxiliar na montagem dos kits (aqueles que puderem chegar antes na cidade)</li>
	<li>Transportar materiais necessários para atividades, dinâmicas ou funcionamento do evento</li>
	<li>Monitorar oficinas e palestras e oferecer ajuda ao oficineiro/palestrante no que for preciso, apresenta-lo aos encontristas e reporta-se à CORDe logo após</li>
	<li>Chegar no local designado com pontualidade para que não haja atrasos na grade horária</li>
	<li>Zelar pelo local e patrimônio a você confiado</li>
	<li>Repassar recados da CORDe aos encontristas e vice-versa</li>
	<li>Comparecer às reuniões de monitoria diárias</li>
</ul>
<p>
A carga horária dos monitores será distribuída entre 3 turnos e terá duração máxima de 8 horas por dia, durante os dias do evento.
 Todo monitor terá no mínimo 1 turno livre por dia. As atividades de monitoria terão início 
no dia 05 e terminarão dia 09 de Novembro. Todos os horários selecionados serão de 
acordo com as necessidades do evento e de cada monitor para com a CORDe. Haverá 
uma planilha pré-evento que será organizada juntamente com o pessoal responsável pela 
monitoria. O período da noite será apenas designado para monitores de festas/noturnos 
e que estes ganharão isenção do pacote de festas.
</p>
<h2>critérios</h2>
<ul>
	<li>Possibilidade de estar com antecedência na cidade do evento</li>
	<li>Habilidades específicas como conhecimentos em TI, desenvoltura para falar em público, pró-atividade</li>
	<li>Experiência em outros eventos (não precisa ser de Design), em outros editais, organização ou como encontrista</li>
	<li>Inscrever-se no prazo estipulado neste edital</li>
	<li>Desenvoltura e comprometimento com as atividades a serem realizadas pelos monitores expostas acima</li>
	<li>Não ser selecionado em nenhum outro edital conflitante</li>
	<li>Trabalhar em grupo</li>
	<li>Flexibilidade de horários</li>
	<li>Disponibilidade para contato pré-evento (e-mail e redes sociais)</li>
	<li>Dedicação e comprometimento com as suas tarefas</li>
	<li>Educação para lidar com pessoas, sempre mantendo o respeito não importando as circunstânceas</li>
	<li>Organização e pontualidade</li>
</ul>
<p>
	Os critérios omissos aos aqui apresentados serão julgados pela CORDe, 
	que possui autonomia para selecionar o candidato de acordo com o que mais for proveitoso.
	Esperamos selecionar os melhores para que o evento seja incrível!
</p>
<h2>é de grátis?</h2>
<p>
	A cada Monitor selecionado será concedida a isenção da inscrição básica, pacote de alojamento e pacote de festas (para os monitores com escalação 
	nas festas). A isenção é intransferível e não cumulativa com outros editais. No caso de 
	seleção em dois ou mais editais, será considerado para fins de isenção o edital com maior 
	número de pacotes isentos.
</p>
<p>
	No ato do credenciamento, cada monitor deverá, obrigatoriamente, deixar um cheque caução no valor referente aos custos dos 
	pacotes isentos. O cheque caução poderá ser recuperado após o cumprimento das 
	atividades acertadas em sua íntegra com o integrante da CORDe responsável por essa 
	atividade.
</p>
<p>
	Caso haja desistência ou o monitor não cumpra com sua responsabilidade 
	durante o evento, o cheque caução será descontado. Caso ocorra alguma desistência 
	ou impossibilidade de participação no evento, e o candidato selecionado na segunda 
	chamada já tenha realizado o pagamento da inscrição como encontrista, este poderá 
	solicitar o ressarcimento e receber o valor original (sem taxas bancárias ou parcelamento) equivalente ao seu direito de isenção, apenas durante o acontecimento do R Misto 
	mediante a apresentação dos documentos de pagamento. Esse valor será equivalente ao 
	pacote pago durante as inscrições.
</p>
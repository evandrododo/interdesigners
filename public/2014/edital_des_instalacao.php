<h2>o que são? onde vivem? o que comem?</h2>
<p>
	O edital de instalação é para as pessoas que gostavam de fazer barraca de lençol, construir casas no the sims e por aí vai. 
	Você gosta de construir coisas? A CORDe estará disponibilizando um espaço para a construção de todo e qualquer tipo de instalação. 
	Você, encontrista, irá construir o que vier nessa sua mente de designer. 
</p>
<p>
	A CORDe irá se responsabilizar pelo espaço no local do evento para auxiliar a visibilidade da exposição da Instalação, 
	bem como a curadoria e a seleção dos trabalhos expostos. A obra pode ser produzida durante o evento ou trazida até o local do evento para a exposição. 
</p>
<h2>critérios</h2>
<p>
	O câmpus oferece diferentes áreas como bosques, áreas gramadas e áreas cobertas como salas e anfiteatros, manteha-se aberto às influências do espaço, experimentando e se apropriando do encontro como um meio de obter um resultado engrandecedor.
	As possibilidades de transformação de um espaço aberto são inúmeras! Considere isso na elaboração da sua atividade e também:
</p>
<ul>
	<li>Inscrever-se no prazo estipulado neste edital</li>
	<li>Adequação ao tema e espaço do evento</li>
	<li>Viabilidade da execução</li>
	<li>Interatividade com o usuário</li>
	<li>Prontidão do usuário em fornecer materiais</li>
	<li>Estética ou funcionalidade</li>
	<li>Representação gráfica do projeto</li>
</ul>
<p>
	A CORDe não se responsabilizará por possíveis danos e alterações no projeto por terceiros ou por ocorridos que fujam do controle dos membros da organização. 
	O local de evento é público e todos que o frequentam podem se sentir no direito de alterar e contribuir com a ambientação do mesmo, sendo esta instalação de total acesso e livre para interação com o público. 
</p>
<p>Os critérios omissos aos aqui apresentados serão julgados pela CORDe, que possui autonomia para selecionar o candidato de acordo com o que mais for proveitoso. Esperamos selecionar os melhores para que o evento seja incrível!</p>
<h2>é de grátis?</h2>
<p>
	Esse edital não possui isenção, pois encaramos como uma cooperação mútua, onde o participante tem o benefício de ver seus trabalhos expostos, como meio de divulgação. E a CORDe vai se responsabilizar por organizar todos os materiais que serão utilizados, então descreva tudo certo, pra não faltar nada.
</p>
<script>
$(document).ready(function(){

	//sorteio de skins
	function skin_home() {
		var numero = Math.floor(Math.random() * 4) + 1;
		while ( $('.faixa').first().hasClass('cor'+numero)==true ) {
			numero = Math.floor(Math.random() * 4) + 1;
		}
		$('.faixa').each(function(){
				if(numero==5) numero=1;
				$(this).removeClass('cor1 cor2 cor3 cor4').addClass('cor'+numero);
				numero++;
		});
	}
	skin_home();
	$('a.logo,.identidade a').click(function(){
		skin_home();
	});

	//easing
	jQuery.extend( jQuery.easing,
	{
		def: 'easeInOutExpo',
		swing: function (x, t, b, c, d) {
			return jQuery.easing[jQuery.easing.def](x, t, b, c, d);
		},
		easeInOutExpo: function (x, t, b, c, d) {
			if (t==0) return b;
			if (t==d) return b+c;
			if ((t/=d/2) < 1) return c/2 * Math.pow(2, 10 * (t - 1)) + b;
			return c/2 * (-Math.pow(2, -10 * --t) + 2) + b;
		}
	});
	$('.nav a').click(function(event) {
		event.preventDefault();
		$('html, body').stop().animate({scrollTop: $($(this).attr('href')).offset().top-400}, 1000,'easeInOutExpo');
	});

	$(window).scroll(function(){
		if($(this).scrollTop()>150) {
			$('.nav').addClass('fixo');
		} else {
			$('.nav').removeClass('fixo');
		};
	});
	
});
</script>

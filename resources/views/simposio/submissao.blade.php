<h3>Orientações</h3>

<p>
Serão aceitos apenas trabalhos de alunos de graduação, recém formado ou alunos especiais de mestrado como primeiro autor. Ou seja, não serão aceitos trabalhos de alunos de mestrado já matriculado, doutorado e professores. A intenção deste evento é motivar alunos mais jovens a adentrar na vida acadêmica. Um evento feito de estudantes para estudantes de Design e áreas afins.</p>
<p>Os trabalhos para o II Simpósio Interdesigners devem se encontrar nas seguintes temáticas e grandes áreas:</p>

<ul>
<li>Design de Produto </li>
<li>Design Gráfico </li>
<li>Design Inclusivo </li>
<li>Design Informacional </li>
<li>Design Urbano </li>
<li>Eco Design (design sustentável) </li>
<li>Ergonomia e Acessibilidade </li>
<li>Fundamentos Teóricos </li>
<li>História do Design </li>
<li>Materiais e processo </li>
<li>Comunicação Visual </li>
</ul>
<p>
Eles devem ser enviados em formato .pdf de acordo com o <b><a href="https://drive.google.com/file/d/0By1o8AZJGyKMQWZERFh5blNNM0E/view?usp=sharing" target="_blank">template disponível</a></b> de modo avaliação cega. Os trabalhos que não estiverem neste formato não serão avaliados.
</p>
<p>Seu trabalho deve ser nomeado da seguinte maneira: áreadotrabalho_títulodotrabalho.pdf<br><br>
Por exemplo:<br>
Semiótica_PosiçõesCulturaisImpostasPorCadeiras.pdf<br>
DesignGráfico_CartazesDoSéculoXX.pdf<br>
DesignUrbano_MobiliárioUrbanoNaCidadeDeSãoPaulo.pdf<br>
<p>
Para que a mesma aconteça, cobraremos uma taxa de submissão no valor de R$ 10,00 (dez reais). O pagamento deverá fazer um depósito na conta do Banco do Brasil, agência 6533-1, conta poupança 16.931-5, variação 51. Este valor deve ser pago anteriormente ao envio, pois o comprovante do depósito deve constar no momento que o trabalho for submetido. Se houver mais de um trabalho do mesmo autor, o pagamento e submissão deve ser feito de maneira separada. Não fazer pagamentos em conjuntos pois a submissão deve ser feita separadamente.
</p>
<p>
Se seu trabalho for aceito ou negado o feedback e correções chegará no email do qual você submeteu o trabalho. Caso seja aceito, corrija-o conforme necessidade e indicações do comitê científico e envie o arquivo final em formato .doc para interdesigners.unesp@gmail.com dentro do template do evento, já com nome dos autores, email e instituição de origem. O assunto do email deverá ser ARTIGO SIMPÓSIO e nomeado da seguinte forma: áreadotrabalho_títulodotrabalho_final.doc
</p>
<p>
Todos os trabalhos aceitos serão apresentados em <a href="https://drive.google.com/file/d/0By1o8AZJGyKMS2VsWllRR2s3TVk/view?usp=sharing">forma de pôster</a> no evento. A impressão pode ser feita pela comissão ou pelos autores. Caso desejem que a impressão seja feita pela organização do evento, será cobrado o valor de R$ 40,00 (quarenta reais) cada impressão e devem ser enviados os pôsteres em formato .pdf até 30/10 às 12h no email interdesigners.unesp@gmail.com, entitulado áreadotrabalho_títulodotrabalho_poster.pdf, com o assunto POSTER SIMPÓSIO, junto com o comprovante do depósito de pagamento de impressão.
</p>
<p>
Pelo menos um dos autores dos trabalhos deverá estar inscrito no evento até dia 20/10 para que o trabalho seja publicado nos anais do mesmo.
</p>
